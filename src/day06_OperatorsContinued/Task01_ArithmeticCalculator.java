package day06_OperatorsContinued;

public class Task01_ArithmeticCalculator {
public static void main(String[] args) {
	
	// Arithmetic Calculator
	//	Write a Java program that takes two numbers as input from 
	//	 the user and performs arithmetic operations. 
	//   (addition, subtraction, multiplication, division, and modulus)
	//    using different operators.
	
	int numA = 50;
	int numB = 2;
	
	int userAdd = numA + numB;
	int userSub = numA - numB;
	int userMulti = numA * numB;
	int userDivide = numA / numB;
	int userMod = numA % numB;
	
	System.out.println(userAdd);
	System.out.println(userSub);
	System.out.println(userMulti);
	System.out.println(userDivide);
	System.out.println(userMod);
	
	
	
	
	
	
}
}
