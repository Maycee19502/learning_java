package day03_variables;

public class PrimitiveType_FloatAndDouble {

	public static void main(String[] args) {
		
		
		float price = 9.99f; //When using float you must include f at the end of your value
		
		System.out.println(price);
		
		double accountBalance = 9999.99; 
		
		System.out.println(accountBalance); 
	}
}
