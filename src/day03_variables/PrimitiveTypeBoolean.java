package day03_variables;

public class PrimitiveTypeBoolean {

	public static void main(String[] args) {
		
	// Boolean stores true and false value only
	// dataType variable = value;
		
		boolean testResult = true;
		boolean resultOfTestCase101 = false;
		boolean isMondayToday = false;
		boolean isSunnyToday = true;
		
		System.out.println(testResult);// prints TRUE because variable hold true value
		System.out.println(resultOfTestCase101);// prints TRUE because variable hold true value
		System.out.println(isMondayToday);// prints FALSE because variable hold true value
		System.out.println(isSunnyToday);// prints TRUE because variable hold true value
		
		

	}

}
