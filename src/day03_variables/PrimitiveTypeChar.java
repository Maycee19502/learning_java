package day03_variables;

public class PrimitiveTypeChar {

	public static void main(String[] args) {
		
 // Char DataType holds only a single character (1 letter, 1 number, 1 special character) 
 // Char DataType value is in single quotation marks

		char letter ='a';
		char digit = '9';
		char letter2 ='^';
		char letter3 = ' ';
		
		System.out.println(letter); // a
		System.out.println(letter2); // 9
		System.out.println(letter3); // ^
		System.out.println(digit);  // empty space
		

	}

}
